def label_for(name: str) -> str:
    if name in FEATURE_LABELS:
        return FEATURE_LABELS[name]
    return name.replace("_", " ").strip().title()


def description_for(name: str) -> str | None:
    return FEATURE_DESCRIPTIONS.get(name)
